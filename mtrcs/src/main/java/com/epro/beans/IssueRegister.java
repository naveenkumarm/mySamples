package com.epro.beans;

public class IssueRegister {
	private String issueId; 
	private String dateRaised; 
	private String raisedBy;
	private String status;
	private String descriptionOfIssue; 
	private String additionalInfo;
	private String impactRating;
	private String priority;
	private String action;
	private String assignedTo;
	private String dateForResolution;
	private String resolutionDate;
	
	public String getIssueId() {
		return issueId;
	}
	public void setIssueId(String issueId) {
		this.issueId = issueId;
	}
	public String getDateRaised() {
		return dateRaised;
	}
	public void setDateRaised(String dateRaised) {
		this.dateRaised = dateRaised;
	}
	public String getRaisedBy() {
		return raisedBy;
	}
	public void setRaisedBy(String raisedBy) {
		this.raisedBy = raisedBy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDescriptionOfIssue() {
		return descriptionOfIssue;
	}
	public void setDescriptionOfIssue(String descriptionOfIssue) {
		this.descriptionOfIssue = descriptionOfIssue;
	}
	public String getAdditionalInfo() {
		return additionalInfo;
	}
	public void setAdditionalInfo(String additionalInfo) {
		this.additionalInfo = additionalInfo;
	}
	public String getImpactRating() {
		return impactRating;
	}
	public void setImpactRating(String impactRating) {
		this.impactRating = impactRating;
	}
	public String getPriority() {
		return priority;
	}
	public void setPriority(String priority) {
		this.priority = priority;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getDateForResolution() {
		return dateForResolution;
	}
	public void setDateForResolution(String dateForResolution) {
		this.dateForResolution = dateForResolution;
	}
	public String getResolutionDate() {
		return resolutionDate;
	}
	public void setResolutionDate(String resolutionDate) {
		this.resolutionDate = resolutionDate;
	}
	public void setAssignedTo(String assignedTo) {
		this.assignedTo = assignedTo;
	}
	public String getAssignedTo() {
		return assignedTo;
	}
}
